type TranslationObject = typeof translations.en;
export type TranslationKey = keyof TranslationObject;

export const useTranslation = (lang: LanguageCode) => {
  const languageName = {
    en: 'English',
    hi: 'Hindi',
    ta: 'Tamil',
    kn: 'Kannada',
    bn: 'Bengali',
    mr: 'Marathi',
    gu: 'Gujarati',
    ml: 'Malayalam',
    te: 'Telugu',
    ur: 'Urdu',
  }[lang];

  const t = (key: TranslationKey): string => {
    return translations[lang]?.[key] || translations.en[key];
  };
  
  t.languageName = languageName;
  return t as typeof t & { languageName: string };
};

export default translations;
